import java.util.Scanner;

public class oitavoProjeto {
    /// vamo aprender sobre operadores arentimetico nessa aula
    /// + / - / * / '/' / % , depender do operando, seu valor de divisão vai ser referente ao operando
    
    public static void main(String[] args) {
        int x = 9;
        int y = 2;
        System.out.printf("x + y = %d\n", x + y);
        System.out.printf("x - y = %d\n", x - y);
        System.out.printf("x * y = %d\n", x * y);
        System.out.printf("x / y = %d\n", x / y); // divisão inteira
        System.out.printf("x / y = %2.2f\n", x / (double) y);// divisão normal
        System.out.printf("x %% y = %d\n", x % y);
    }
}

