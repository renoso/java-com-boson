public class nonoProjeto {

    //operadores relacionais

    public static void main(String[] args) {
        
        int x = 10;
        int k = 20;
        System.out.printf("é igual a 10? %b\n", x == 10);
        System.out.printf("é diferente de 10? %b\n", x != 10);
        System.out.printf("é maior que k? %b\n", x > 10);
        System.out.printf("é manor que 10? %b\n", x >= 10);
        System.out.printf("é menor que 10? %b\n", x < 10);
        System.out.printf("é menor ou igual a k? %b", x <= k);
    }
}